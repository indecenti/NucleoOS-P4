static const char *const kNativeName[NV_LANG_COUNT] = {
    [NV_LANG_EN] = "English",
    [NV_LANG_IT] = "Italiano",
    [NV_LANG_ES] = "Español",
    [NV_LANG_FR] = "Français",
    [NV_LANG_DE] = "Deutsch",
};

void nv_i18n_init(void) {
    int l = nv_config_get_int("lang", NV_LANG_EN);
    if (l < 0 || l >= NV_LANG_COUNT) l = NV_LANG_EN;   // clamp stale/corrupt values
    s_lang = (nv_lang_t)l;
    NV_LOGI("i18n", "language = %s", kNativeName[s_lang]);
}

void nv_i18n_set_lang(nv_lang_t l) {
    if (l < 0 || l >= NV_LANG_COUNT || l == s_lang) return;   // ignore invalid / no-op
    s_lang = l;
    nv_config_set_int("lang", (int)l);
    NV_LOGI("i18n", "language -> %s", kNativeName[s_lang]);
    nv_event_publish(NV_EV_LANG_CHANGED, &s_lang);
}

nv_lang_t nv_i18n_get_lang(void) { return s_lang; }

const char *nv_tr(nv_str_id_t id) {
    if (id < 0 || id >= NV_STR_COUNT) return "";
    const char *s = kStr[s_lang][id];
    if (s && s[0]) return s;
    const char *en = kStr[NV_LANG_EN][id];   // fall back to English on missing/empty
    return en ? en : "";
}

const char *nv_lang_native_name(nv_lang_t l) {
    if (l < 0 || l >= NV_LANG_COUNT) return "";
    return kNativeName[l];
}

// ---- localized short calendar names (ASCII-safe: no accents, so any built font renders them) --
static const char *const kWday[NV_LANG_COUNT][7] = {
    [NV_LANG_EN] = {"Sun","Mon","Tue","Wed","Thu","Fri","Sat"},
    [NV_LANG_IT] = {"Dom","Lun","Mar","Mer","Gio","Ven","Sab"},
    [NV_LANG_ES] = {"Dom","Lun","Mar","Mie","Jue","Vie","Sab"},
    [NV_LANG_FR] = {"Dim","Lun","Mar","Mer","Jeu","Ven","Sam"},
    [NV_LANG_DE] = {"So","Mo","Di","Mi","Do","Fr","Sa"},
};
static const char *const kMonth[NV_LANG_COUNT][12] = {
    [NV_LANG_EN] = {"Jan","Feb","Mar","Apr","May","Jun","Jul","Aug","Sep","Oct","Nov","Dec"},
    [NV_LANG_IT] = {"Gen","Feb","Mar","Apr","Mag","Giu","Lug","Ago","Set","Ott","Nov","Dic"},
    [NV_LANG_ES] = {"Ene","Feb","Mar","Abr","May","Jun","Jul","Ago","Sep","Oct","Nov","Dic"},
    [NV_LANG_FR] = {"Jan","Fev","Mar","Avr","Mai","Jui","Jul","Aou","Sep","Oct","Nov","Dec"},
    [NV_LANG_DE] = {"Jan","Feb","Mrz","Apr","Mai","Jun","Jul","Aug","Sep","Okt","Nov","Dez"},
};

const char *nv_i18n_wday_short(int wday) {
    if (wday < 0 || wday > 6) return "";
    const char *s = kWday[s_lang][wday];
    return (s && s[0]) ? s : kWday[NV_LANG_EN][wday];
}
const char *nv_i18n_month_short(int mon) {
    if (mon < 0 || mon > 11) return "";
    const char *s = kMonth[s_lang][mon];
    return (s && s[0]) ? s : kMonth[NV_LANG_EN][mon];
}
